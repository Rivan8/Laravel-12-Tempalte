    <h1>Kebijakan Privasi (Privacy Policy)</h1>
    <p><strong>Terakhir diperbarui:</strong> 02 April 2026</p>

    <p>Elshaddai Learning Center ("kami", "kita") mengoperasikan platform web dan aplikasi mobile MyESC Learning Center (selanjutnya disebut "Layanan").</p>
    <p>Halaman ini memberi tahu Anda tentang kebijakan kami terkait pengumpulan, penggunaan, dan pengungkapan data pribadi saat Anda menggunakan Layanan kami serta pilihan yang Anda miliki terkait data tersebut.</p>

    <h2>1. Pengumpulan dan Penggunaan Informasi</h2>
    <p>Kami mengumpulkan beberapa jenis informasi untuk berbagai tujuan guna menyediakan dan meningkatkan Layanan kami kepada Anda. Layanan ini adalah platform Learning Management System (LMS) internal gereja sehingga membutuhkan pembuatan akun sebelum pengguna dapat mengakses kelas.</p>

    <h3>Jenis Data yang Dikumpulkan:</h3>
    <ul>
        <li><strong>Data Pribadi:</strong> Saat menggunakan Layanan kami, kami mungkin meminta Anda untuk memberikan informasi identitas pribadi (seperti Alamat Email, Nama Depan dan Belakang, Nomor Telepon/WhatsApp).</li>
        <li><strong>Data Aktivitas & Progres:</strong> Untuk tujuan pembelajaran (LMS), kami melacak kemajuan Anda di setiap materi kelas (persentase video yang ditonton, nilai kuis, status penyelesaian kelas) untuk menaikkan tingkat kedewasaan rohani Anda di sistem kami.</li>
    </ul>

    <h2>2. Penggunaan Data</h2>
    <p>MyESC Learning Center menggunakan data yang dikumpulkan untuk berbagai tujuan:</p>
    <ul>
        <li>Untuk menyediakan dan memelihara Layanan LMS.</li>
        <li>Untuk mengidentifikasi Anda sebagai jemaat/murid yang sah di dalam basis data (database) gereja.</li>
        <li>Untuk memantau kemajuan kurikulum (Dasar, Tertanam, Pertumbuhan, Dampak) Anda.</li>
        <li>Untuk memberikan dukungan layanan pelanggan.</li>
    </ul>

    <h2>3. Keamanan Data</h2>
    <p>Keamanan data Anda penting bagi kami, tetapi ingatlah bahwa tidak ada metode transmisi melalui Internet, atau metode penyimpanan elektronik yang 100% aman. Server kami dilengkapi dengan perlindungan otentikasi Bearer Token (Sanctum) berlapis, namun kami tidak dapat menjamin keamanan mutlaknya.</p>

    <h2>4. Permintaan Penghapusan Data (Account Deletion)</h2>
    <p>Jika Anda tidak lagi tergabung dalam komunitas Elshaddai dan ingin informasi akun, nama, dan detail penyelesaian kelas Anda dihapus sepenuhnya dari server kami, Anda dapat mengajukan permintaan penghapusan data dengan mengirim email kepada Admin kami di: <strong>admin@myesc.id</strong> atau melalui Fasilitator/gembala Anda yang berwenang.</p>

    <h2>5. Perubahan Kebijakan Privasi Ini</h2>
    <p>Kami dapat memperbarui Kebijakan Privasi kami dari waktu ke waktu. Kami akan memberi tahu Anda tentang perubahan apa pun dengan memposting Kebijakan Privasi yang baru di halaman ini.</p>

    <h2>6. Hubungi Kami</h2>
    <p>Jika Anda memiliki pertanyaan tentang Kebijakan Privasi ini, silakan hubungi kami:</p>
    <ul>
